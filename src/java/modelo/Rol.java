package modelo;

/**
 *
 * @author julio
 */
public class Rol {
    private int codigoRol ;
    private String Rol;

    public Rol() {
    }

    public Rol(int codigoRol, String Rol) {
        this.codigoRol = codigoRol;
        this.Rol = Rol;
    }

    public int getCodigoRol() {
        return codigoRol;
    }

    public void setCodigoRol(int codigoRol) {
        this.codigoRol = codigoRol;
    }

    public String getRol() {
        return Rol;
    }

    public void setRol(String Rol) {
        this.Rol = Rol;
    }
    
    

}